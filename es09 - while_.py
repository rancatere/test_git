numero = int(input("Inserisci un numero: "))
somma = numero

while numero != 0:
    numero = int(input("Inserisci un numero: "))
    somma += numero
    print(f"Somma: {somma}")
#chiediamo altri numeri da sommare finchè l'utente mette 0, è la condizione
#che fa sì che noi continuiamo a ripetere il ciclo
#se scrive 0 esce dal ciclo e stampa Programma finito
print("Programma finito")

#ciclo ripetuto finchè la condizione è vera=while true



